#include <stdio.h>

int main()
{
    int a = 2 + 31;
    int b = 45 * 45 - 11;
    float c = 15 / 4;
    float d = 15.0 / 4;
    float e = 67 % 5;
    float f = (2 * 45.1 + 3.2) / 2;

    printf("2 + 31 = %u\n", a);
    printf("45 * 45 - 11 = %u\n", b);
    printf("15 / 4 = %f\n", c);
    printf("15.0 / 4 = %f\n", d);
    printf("67 \% 5 = %f\n", e);
    printf("2 * 45.1 + 3.2 = %f\n", f);
}